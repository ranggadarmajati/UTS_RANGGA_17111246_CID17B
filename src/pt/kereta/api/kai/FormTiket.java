    public void ValidasiAngka(KeyEvent b)
    {
        if(Character.isAlphabetic(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null, "Inputan hanya boleh angka", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    public void ValidasiForm(){
        if(NamaPemesan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Pemesan tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
            NamaPemesan.requestFocus();
        }else if(jComboBoxStasiunAsal.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "Stasiun Asal harus dipilih", "Peringatan", JOptionPane.WARNING_MESSAGE);
            jComboBoxStasiunAsal.requestFocus();
        }else if(jComboBoxStasiunTujuan.getSelectedItem().equals("")){
            jComboBoxStasiunTujuan.requestFocus();
        }else if(kode_kereta.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Tiket Kereta harus dipilih", "Peringatan", JOptionPane.WARNING_MESSAGE);
            jTableTiket.requestFocus();
        }else if((int) jml_dws.getValue() < 1){
            JOptionPane.showMessageDialog(null, "Jumlah Penumpang Dewasa harus diisi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            jml_dws.requestFocus();
        }else if(nama_penumpang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Penumpang tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
            nama_penumpang.requestFocus();
        }else if(no_ktp.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No KTP tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
            no_ktp.requestFocus();
        }else if(kls_executive.isSelected() == false && kls_bisnis.isSelected() == false && kls_ekonomi.isSelected() == false){
            JOptionPane.showMessageDialog(null, "Kelas harus dipilih", "Peringatan", JOptionPane.WARNING_MESSAGE);
            kls_executive.requestFocus();
        }else{
            if (JOptionPane.showConfirmDialog(null, "Apakah Data Yang Anda sudah benar ?", "Peringatan", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                CreateUpdateDelete("create");
                CreateUpdateDelete("get_all");
                setNullValue();
            }
        }
    }
    
