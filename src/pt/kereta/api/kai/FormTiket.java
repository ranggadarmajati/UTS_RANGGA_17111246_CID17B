    public void getTiketListCount(String sta, String stu)
    {
        java.util.Date Tglberangkat = TGLBERANGKAT.getDate();
        java.sql.Date tgl_berangkat = new java.sql.Date(Tglberangkat.getTime());
        try{
            sql = "SELECT \n" +
                  "count(tt.id) as rowcount \n" +
                  "FROM `t_tiket` tt \n" +
                  "JOIN t_stasiun ts ON ts.id = tt.id_st_asal\n" +
                  "JOIN t_stasiun tst ON tst.id = tt.id_st_tujuan\n" +
                  "JOIN t_kereta tk ON tk.id = tt.id_kereta\n" +
                  "WHERE ts.nama = ? AND tst.nama = ? and tt.tgl_brangkat= ?";
            pst=con.prepareStatement(sql);
                pst.setString(1, sta);
                pst.setString(2, stu);
                pst.setDate(3, tgl_berangkat);
            rs =pst.executeQuery();
            rs.next();
            int count = rs.getInt("rowcount");
            rs.close();
            if(count < 1){
                JOptionPane.showMessageDialog(this, "Tiket untuk tanggal tersebut tidak tersedia!");
                DefaultTableModel tbl= new DefaultTableModel();
                tbl.addColumn("Id");
                tbl.addColumn("Stasiun Asal");
                tbl.addColumn("Stasiun Tujuan");
                tbl.addColumn("Tanggal Berangkat");
                tbl.addColumn("Jam Berangkat");
                tbl.addColumn("Jam Tiba");
                tbl.addColumn("Kereta");
                tbl.addColumn("Kursi Tersedia");
                jTableTiket.setModel(tbl);
                tbl.removeRow(2);
                
            }else{
                getTiket(sta, stu);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
