    public void CreateUpdateDelete(String type)
    {
        switch (type) {
            case "create":
                try{
                    sql = "INSERT INTO `t_order` (`no_ktp`, `nama_pemesan`, `nama_penumpang`, `id_tiket`, `id_kelas`) VALUES (?,?,?,?,?)";
                    pst=con.prepareStatement(sql);
                    pst.setString(1, no_ktp.getText());
                    pst.setString(2, NamaPemesan.getText());
                    pst.setString(3, nama_penumpang.getText());
                    pst.setString(4, idtkt.getText());
                    pst.setInt(5, idKelas);
                    pst.execute();
                    
                    JOptionPane.showMessageDialog(this, "Data Berhasil disimpan.");
                } catch (SQLException err){
                    JOptionPane.showMessageDialog(this, err.getMessage());
                }
                break;
            case "hapus":
                try{
                    sql = "DELETE FROM `t_order` WHERE id=?";
                    pst=con.prepareStatement(sql);
                    pst.setString(1, id_order.getText());
                    pst.execute();
                    JOptionPane.showMessageDialog(this, "Data Berhasil dihapus.");
                } catch (SQLException err) {
                    JOptionPane.showMessageDialog(this, err.getMessage());
                }   break;
            case "get_all":
                DefaultTableModel tb= new DefaultTableModel();
                tb.addColumn("Id");
                tb.addColumn("No Ktp");
                tb.addColumn("Nama Penumpang");
                tb.addColumn("Jadwal Berangkat");
                tb.addColumn("Stasiun Asal");
                tb.addColumn("Stasiun Tujuan");
                tb.addColumn("Kelas");
                tb.addColumn("Kereta");
                jTable1.setModel(tb);
                try{
                    sql = "SELECT \n" +
                            "tor.id, tor.no_ktp, \n" +
                            "tor.nama_penumpang, \n" +
                            "concat(tt.tgl_brangkat,\" \", tt.jam_brangkat, \" - \", tt.jam_tiba) as jadwal_berangkat, \n" +
                            "ts.nama st_asal, \n" +
                            "tst.nama st_tujuan, \n" +
                            "tkls.nama kelas,\n" +
                            "tk.nama kereta\n" +
                            "FROM `t_order` tor\n" +
                            "JOIN t_tiket tt ON tt.id = tor.id_tiket\n" +
                            "JOIN t_kelas tkls ON tkls.id = tor.id_kelas\n" +
                            "JOIN t_stasiun ts ON ts.id = tt.id_st_asal\n" +
                            "JOIN t_stasiun tst ON tst.id = tt.id_st_tujuan\n" +
                            "JOIN t_kereta tk ON tt.id_kereta = tk.id";
                    pst =con.prepareStatement(sql);
                    rs = pst.executeQuery();
                    
                    while(rs.next()){
                        tb.addRow(new Object[]{
                            rs.getString("id"),
                            rs.getString("no_ktp"),
                            rs.getString("nama_penumpang"),
                            rs.getString("jadwal_berangkat"),
                            rs.getString("st_asal"),
                            rs.getString("st_tujuan"),
                            rs.getString("kelas"),
                            rs.getString("kereta")
                        });
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }   break;
            default:
                break;
        }
    }
    
